export function SocialLinks({
  socials,
  className,
}: {
  socials: { platform: string; url: string }[]
  className?: string
}) {
  if (!socials.length) return null
  return (
    <div className={className}>
      <h3 className="text-sm font-semibold text-gray-900">Follow this shop</h3>
      <div className="mt-2 flex flex-wrap gap-2">
        {socials.map(({ platform, url }) => {
          const brand = PATHS[platform]
          return (
            <a
              key={platform}
              href={url}
              target="_blank"
              rel="noopener noreferrer nofollow"
              aria-label={brand?.label ?? platform}
              title={brand?.label ?? platform}
              className="group flex h-9 w-9 items-center justify-center rounded-full border border-gray-200 bg-white text-gray-500 transition-colors hover:border-gray-300 hover:bg-gray-50"
            >
              {brand ? (
                <svg
                  width={17}
                  height={17}
                  viewBox="0 0 24 24"
                  fill="currentColor"
                  aria-hidden="true"
                  className="transition-colors"
                  style={{ color: 'inherit' }}
                >
                  <path d={brand.path} />
                </svg>
              ) : (
                <Globe width={17} height={17} />
              )}
            </a>
          )
        })}
      </div>
    </div>
  )
}
